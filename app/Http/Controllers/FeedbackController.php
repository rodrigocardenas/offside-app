    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $validated = $request->validate([
            'message' => 'required|string|min:10|max:1000',
            'type' => 'required|in:suggestion,bug,compliment,other',
            'is_anonymous' => 'sometimes|boolean'
        ]);

        $feedback = new Feedback();
        $feedback->user_id = Auth::id();
        $feedback->message = $validated['message'];
        $feedback->type = $validated['type'];
        $feedback->is_anonymous = $request->boolean('is_anonymous', false);
        $feedback->save();

        return redirect()->back()->with('success', 'Feedback enviado correctamente');
    }

